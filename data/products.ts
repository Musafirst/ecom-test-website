import type { JammProduct, ProductCategory, ProductCollection, ProductSubcategory } from '@/types/product'

type LiveFallbackProduct = {
  handle: string
  title: string
  price: number
  category: ProductCategory
  categoryLabel: string
  image: string
  brand: string
  collection?: ProductCollection
  subcategory?: ProductSubcategory
  tags?: string[]
}

function product(item: LiveFallbackProduct): JammProduct {
  return {
    id: item.handle,
    handle: item.handle,
    title: item.title,
    price: item.price,
    currencyCode: 'USD',
    availableForSale: true,
    collection: item.collection,
    category: item.category,
    categoryLabel: item.categoryLabel,
    subcategory: item.subcategory,
    tags: item.tags ?? [],
    brand: item.brand,
    image: item.image,
    imageAlt: `${item.title} product image`,
    galleryImages: [item.image],
  }
}

